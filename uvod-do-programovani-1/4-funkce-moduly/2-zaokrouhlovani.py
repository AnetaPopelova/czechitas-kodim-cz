# Divadlo chce mít ceny vstupenek jak v eurech tak v celých korunách.
# Uložte do proměnné eura cenu studentské vstupenky (65% z 12 euro).

eura = 12 * 0.65


# Použijte funkci round() a do proměnné koruny spočítejte kolik činí studentské vstupné v korunách jestliže kurz eura je 24 korun.

koruny = 24 * eura

koruny = round(24 * eura)
